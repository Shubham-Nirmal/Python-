# Duplicate values ठेवू शकतो.

# Index वापरून elements access करू शकतो.



# 1. List:
# Mutable – Elements in a List can be changed.

# Written in square brackets []

# Can contain duplicate values

# Can access elements using an index.

frutes = ["Apple", "Banana", "Cherry", "Date"]

print(frutes)  # Output: ['Apple', 'Banana', 'Cherry', 'Date']
print(frutes[0])  # Output: Apple (accessing the first element)
print(frutes[1])  # Output: Banana (accessing the second element)
frutes[2] = "Mango"  # Changing the third element
print(frutes)  # Output: ['Apple', 'Banana', 'Mango', 'Date'] (after modification)




