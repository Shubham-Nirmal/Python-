#################### SETS IN Python ####################
# Sets म्हणजे unordered collections of unique elements, म्हणजेच त्यात duplicate elements नसतात.
# Sets are unordered collections of unique elements, meaning they do not allow duplicate elements.

# set is a collection of defined objects


s = set()  # empty set 


e = {1,2,3,3,3,3,4,5,6,7,8,9}  # set with multiple elements
print(e)  # Output: {1, 2, 3, 4, 5, 6, 7, 8, 9} (duplicates removed)
# Sets are mutable, meaning you can add or remove elements after creation.



