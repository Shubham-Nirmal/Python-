# Arithmetic Operators
# Arithmetic operators are used to perform mathematical operations on variables and values.
# The basic arithmetic operators are +, -, *, /, //, %, and **.

# Addition
a = 10

b = 5
c = a + b
print("Sum:", c)

# Subtraction

d = a - b
print("Difference:", d)

# Multiplication

e = a * b
print("Product:", e)

# Division

f = a / b
print("Quotient:", f)

# Floor Division

g = a // b
print("Floor Quotient:", g)

# Modulus

h = a % b
print("Remainder:", h)

# Exponentiation

i = a ** b
print("Power:", i)




# Assignment Operators


x = 10
y = 5
x += 5
y -= 2
print("x:", x)
print("y:", y)

# Comparison Operators
a = 10
b = 5
print("Equal:", a == b)
print("Not Equal:", a != b)
print("Greater:", a > b)
print("Less:", a < b)
print("Greater or Equal:", a >= b)
print("Less or Equal:", a <= b)   


# Logical Operators
print("Logical AND:", a > 5 and b < 10)
print("Logical OR:", a > 5 or b < 10)
print("Logical NOT:", not(a > 5)) 


# truth table of 'or'
print("True or True:", True or True)  # True
print("True or False:", True or False)  # True
print("False or True:", False or True)  # True
print("False or False:", False or False)  # False

# truth table of 'and'
print("True and True:", True and True)  # True
print("True and False:", True and False)  # False
print("False and True:", False and True)  # False
print("False and False:", False and False)  # False

