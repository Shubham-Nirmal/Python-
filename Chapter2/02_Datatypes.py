# Data types in Python
# In Python, data types are used to define the type of a variable.
# The main built-in data types are:
# 1. Integers
# 2. Floats
# 3. Strings
# 4. Booleans
# 5. NoneType

a = 1 #a is an integer
b = 2.5 #b is a float
c = "Hello" #c is a string
d = True #d is a boolean
e = None #e is a NoneType
print(a, b, c, d, e)                                