############## Relational Operators in Python ####################

# Relational operators म्हणजेच तुलना करणारे operators. Python मध्ये, आपण ==, !=, >, <, >=, <= या operators चा वापर करून दोन values चा संबंध तपासू शकतो.

# 1. Equal to (==) operator:
a = 10
b = 20
if (a == b):
    print("Both are equal.")
else:
    print("Both are not equal.")

# 2. Not equal to (!=) operator:
c = 30
d = 30
if (c != d):
    print("Values are different.")
else:
    print("Values are same.")

# 3. Greater than (>) operator:
e = 40
f = 20
if (e > f):
    print("e is greater than f.")
else:
    print("e is not greater than f.")

# 4. Less than (<) operator:
g = 10
h = 20
if (g < h):
    print("g is less than h.")
else:
    print("g is not less than h.")

# 5. Greater than or equal to (>=) operator:
i = 30
j = 30
if (i >= j):
    print("i is greater than or equal to j.")
else:
    print("i is not greater than or equal to j.")

# 6. Less than or equal to (<=) operator:
k = 20
l = 30
if (k <= l):
    print("k is less than or equal to l.")
else:
    print("k is not less than or equal to l.")
